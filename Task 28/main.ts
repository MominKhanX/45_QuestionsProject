// Creating Varible
let age = 100;


// Creating A Program for Determining the stage of life using Else-If chain
if (age < 2){
    console.log("He is a Baby.");
}
else if (age >= 2 && age < 4){
    console.log("He is a Toddler.");
}
else if (age >= 4 && age < 13){
    console.log("He is a Kid.");
}
else if (age >= 13 && age < 20){
    console.log("He is a Teenager.");
}
else if (age >= 20 && age < 65){
    console.log("He is an Adult.");
}
else if (age >= 65){
    console.log("He is an Elder.");
}

